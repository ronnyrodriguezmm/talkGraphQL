package com.vass.talks.graphql.model;

public class CounterEvent {
    private String name;
    private Integer value;

    public CounterEvent(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }
}
